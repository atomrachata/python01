#for จะเป็น definite Loop หรือ LOOpที่มีการท่างานที่ชัดเจน
#for กับ
for i in range(1,11,1):
    print(i)
print('finish')
#for กับ List
list1 = ["apple", "blueberry", 'kiwi','papaya']
for element in list1:
    print(element)
#for กับ dic
dic1 = {'name' : 'rachata', 'age' :17, ' hobbies' : ' guitar'}
for key,value in dic1.items():
    print(key,value)
#while indefinite Loop หรือ Loopที่ทำงานไม่ชัดเจน
i =1
while i<10:
    print(i)
    i +=1
